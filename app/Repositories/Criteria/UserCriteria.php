namespace App\Repositories\Criteria;

use Illuminate\Http\Request;
use Prettus\Repository\Contracts\CriteriaInterface;
use Prettus\Repository\Contracts\RepositoryInterface;

class UserCriteria implements CriteriaInterface
{
    /**
     * @var \Illuminate\Http\Request
     */
    protected $request;

    public function __construct(Request $request)
    {
        $this->request = $request;
    }

    /**
     * @param $model
     * @param RepositoryInterface $repository
     *
     * @return mixed
     */
    public function apply($model, RepositoryInterface $repository)
    {
        if ($this->request->filled('name')) {
            $model = $model->where('name', '=', $this->request->get('name'));
        }

        if ($this->request->filled('email')) {
            $model = $model->where('email', '=', $this->request->get('email'));
        }

        if ($this->request->filled('nickname')) {
            $model = $model->where('nickname', '=', $this->request->get('nickname'));
        }

        if ($this->request->filled('phone')) {
            $model = $model->where('phone', '=', $this->request->get('phone'));
        }

        if ($this->request->filled('status')) {
            $model = $model->where('status', '=', $this->request->get('status'));
        }

        return $model;
    }
}
